    private void jbtnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnguardarActionPerformed
        // TODO add your handling code here:
        arido.setDescripcion(jtfdescripcion.getText().toUpperCase());
//        arido.setCantidad(Double.parseDouble(jtfcantidad.getValue().toString()));
        arido.setUndmed(jcbunidmed.getSelectedItem().toString());
        arido.setPrecio(Double.parseDouble(jtfprecioventa.getValue().toString()));
        arido.setInvinicial(Double.parseDouble(jtfinvinicial.getValue().toString()));
        categoria=listcategoria.get(jcbcategoria.getSelectedIndex());
        arido.setIdcatgoria(categoria.getId());
        if(editar==false){
            //  long id=Long.parseLong(jtabla.getValueAt(jtabla.getSelectedRow(), 0).toString());
            if(daoaridos.duplicate(0, jtfdescripcion.getText().toUpperCase(), "GUARDAR",categoria.getId())==true){
//                arido.setIdempresa(singletonemp.getId());
                daoaridos.insert(arido);

            }else {
                JOptionPane.showMessageDialog(null, "EL ARIDO "+jtfdescripcion.getText()+" YA SE ENCUENTRA REGISTRADA","SISTEMA",
                JOptionPane.INFORMATION_MESSAGE);
            }

        }    else{
            //                    usuario.setUsuario(jtfusuario.getText());
            
            if(daoaridos.duplicate(arido.getId(), jtfdescripcion.getText(), "EDITAR",categoria.getId())==true){
                daoaridos.update(arido);

            }else {
                JOptionPane.showMessageDialog(null, "LA TARIFA "+jtfdescripcion.getText()+" YA SE ENCUENTRA REGISTRADA","SISTEMA",
                    JOptionPane.INFORMATION_MESSAGE);
            }

        }

         listaridos = daoaridos.view(jtabla);
        bloquearjbtn(true, false, false, false, false);
        bloquearjtf(false, false, false,false,false,false);
        limpiarjtf();

    }//GEN-LAST:event_jbtnguardarActionPerformed

